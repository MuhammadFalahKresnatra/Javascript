// Loop Menggunakan While

let mulai = 1;
let selesai = 10;

while (mulai <= selesai) {
    console.log(mulai);

    mulai++;
}

// Loop Menggunakan Do While

let start = 101;
let finish = 110;

do {
    console.log(start);
    start++
} while (start < finish);

// Loop Menggunakan For

let awal = 1;
let akhir = 10;

for (awal; awal < akhir; awal++ ) {
    console.log(awal);
}

console.log("------------------");

for (let i = 1; i < 10; i++) {
    console.log(i);
    
}